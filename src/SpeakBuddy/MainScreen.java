    private void CChooserNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CChooserNextActionPerformed
        storage=new Storage("Student");
        
        if(selected==0){
           JOptionPane.showMessageDialog(null, "You must choose one of the characters!");
       }
       else{
                if(selected== 1){
                    name="Amy";
                }
       
                if(selected==2){
                    name="Peter";
                }
                try {
                    String []infor=new String[4];
                    infor[0]=name;
                    infor[1]=0+"";
                    infor[2]=1+"";
                    infor[3]=false+"";
                    storage.writeUserInfo(infor);
                } catch (IOException ex) {
                    ex.printStackTrace();
                }
            showLayout("IntroducingScreen");
            new atualiza().start();
            }
       
    }//GEN-LAST:event_CChooserNextActionPerformed

