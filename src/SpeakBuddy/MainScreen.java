    private void recsItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recsItemActionPerformed
        String args[]=new String[0];
        String media = "./";
        final Juke juke = new Juke(args.length == 0 ? media : args[0]);
        juke.open();
        JFrame f = new JFrame("My Recordings - SpeakBuddy");
        f.addWindowListener(new WindowAdapter() {
            
            public void windowIconified(WindowEvent e) { 
                juke.credits.interrupt();
            }
        });
        f.setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);
        f.getContentPane().add("Center", juke);
        f.pack();
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        int w = 750;
        int h = 340;
        f.setLocation(screenSize.width/2 - w/2, screenSize.height/2 - h/2);
        f.setSize(w, h);
        f.setResizable(false);
        f.setVisible(true);
        if (args.length > 0) {
            File file = new File(args[0]);
            if (file == null && !file.isDirectory()) {
                System.out.println("usage: java Juke audioDirectory");
            } 
        }
    }//GEN-LAST:event_recsItemActionPerformed

